package composition_challenge;

public class Resolution {

	public int x;
	public int y;
	
	/**
	 * @param x
	 * @param y
	 */
	public Resolution(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	
}
